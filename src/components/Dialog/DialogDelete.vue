<template>
  <v-dialog :value="true" persistent max-width="290">
    <v-card>
      <v-card-title class="text-h5"> Delete task? </v-card-title>
      <v-card-text>Are you sure you want to delete this task?</v-card-text>
      <v-card-actions>
        <v-spacer></v-spacer>
        <v-btn text @click="cancelDelete"> No </v-btn>
        <v-btn color="red darken-1" text @click="onDelete"> Yes </v-btn>
      </v-card-actions>
    </v-card>
  </v-dialog>
</template>

<script>
export default {
  methods: {
    onDelete() {
      this.$emit("delete:item");
    },

    cancelDelete() {
      this.$emit("cancel");
    },
  },
};
</script>
